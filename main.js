var OpenCardPropertiesCommand = class {
  constructor(app, cardService, selection, clipboardAdapter) {
    this.app = app;
    this.cardService = cardService;
    this.selection = selection;
    this.clipboardAdapter = clipboardAdapter;
  }
  async execute() {
    try {
      const textCards = this.selection.filter((node) => {
        try {
          const data = node.getData && node.getData();
          return data && data.type === "text";
        } catch (error) {
          console.warn("Error getting node data:", error);
          return false;
        }
      });
      if (textCards.length === 0) {
        new import_obsidian14.Notice("请选择至少一个文本卡片");
        return;
      }
      if (textCards.length === 1) {
        const modal = new SingleCardPropertiesModal(this.app, textCards[0], this.cardService, this.clipboardAdapter);
        modal.open();
      } else {
        const modal = new CardPropertiesModal(this.app, textCards, this.cardService);
        modal.open();
      }
    } catch (error) {
      console.error("打开卡片属性查看器失败:", error);
      new import_obsidian14.Notice("打开属性查看器失败，请重试");
    }
  }
  canExecute() {
    if (!this.selection || this.selection.length === 0) {
      return false;
    }
    const hasTextCards = this.selection.some((node) => {
      try {
        const data = node.getData && node.getData();
        return data && data.type === "text";
      } catch (error) {
        return false;
      }
    });
    return hasTextCards;
  }
  getDescription() {
    return "查看卡片属性";
  }
};
var CopyCardDimensionsCommand = class {
  constructor(selection) {
    this.selection = selection;
  }
  async execute() {
    try {
      const textCards = this.selection.filter((node) => {
        const data = node.getData && node.getData();
        return data && data.type === "text";
      });
      if (textCards.length === 0) {
        new import_obsidian14.Notice("请选择至少一个文本卡片");
        return;
      }
      const dimensions = textCards.map((card) => {
        const data = card.getData();
        return `${data.width}×${data.height}`;
      });
      const uniqueDimensions = [...new Set(dimensions)].sort();
      let copyText = "";
      if (uniqueDimensions.length === 1) {
        copyText = `统一尺寸: ${uniqueDimensions[0]}`;
      } else {
        copyText = `尺寸列表:
${uniqueDimensions.join("\n")}`;
      }
      const clipboardAdapter = new ClipboardAdapter();
      await clipboardAdapter.writeTextWithNotice(copyText, `已复制 ${textCards.length} 个卡片的尺寸信息`);
    } catch (error) {
      console.error("复制尺寸信息失败:", error);
      new import_obsidian14.Notice("复制失败，请重试");
    }
  }
  canExecute() {
    const hasTextCards = this.selection.some((node) => {
      const data = node.getData && node.getData();
      return data && data.type === "text";
    });
    return this.selection.length > 0 && hasTextCards;
  }
  getDescription() {
    return "复制卡片尺寸";
  }
};

