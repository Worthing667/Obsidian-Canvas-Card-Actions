  async updateBothDimensions(width, height) {
    try {
      await this.cardService.unifyCardSizes([this.card], { width, height });
      this.cardData.width = width;
      this.cardData.height = height;
      this.updateSizeDisplay();
      new import_obsidian13.Notice(`卡片尺寸已更新为 ${width}×${height}px`);
      this.close();
    } catch (error) {
      console.error("更新尺寸失败:", error);
      new import_obsidian13.Notice("更新失败: " + error.message);
    }
  }
